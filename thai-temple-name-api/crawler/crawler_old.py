REGEXP = r'(?<=title=")วัด.*?(?="|\s\()'

class Crawler:
    def __init__(self,url):
        self.url = url

    def get(self):
        self.response = requests.get(self.url).text
        return self

    def trim(self):
        # get only the content of the page 
        # self.response = re.search(r'(?<=<div class="mw-parser-output">).*?(?=</div>)', self.response, re.DOTALL).group(0)
        
        return self

    def extract(self,regex):
        extracted_data = re.findall(regex, self.response)
        return extracted_data

    def get_temples_from_provinces(provinces:list) -> list :
        all_records = []
        for province, url in zip(provinces, [URLs[province] for province in provinces]):
            records = []
            print('Crawling: '+province)
            cl = Crawler(url)
            records = cl.get().trim().extract(REGEXP)
            
            # remove last 2 items : "วัดไทย"
            records = records[:len(records)-2]

            print(f'{len(records)} found')

            all_records += records

        print(f'{len(all_records)} found in total')
        return all_records
